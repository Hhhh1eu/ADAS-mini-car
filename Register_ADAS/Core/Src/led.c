#include "led.h"

// PA5 -> Warning LED

// ====================================
// LED ON
// ====================================

void LED_On(void)
{
    GPIOA->ODR |= (1 << 5);
}

// ====================================
// LED OFF
// ====================================

void LED_Off(void)
{
    GPIOA->ODR &= ~(1 << 5);
}

// ====================================
// LED TOGGLE
// ====================================

void LED_Toggle(void)
{
    GPIOA->ODR ^= (1 << 5);
}
